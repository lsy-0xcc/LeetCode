/**
 * Definition for singly-linked list.
 * public class ListNode {
 *     int val;
 *     ListNode next;
 *     ListNode() {}
 *     ListNode(int val) { this.val = val; }
 *     ListNode(int val, ListNode next) { this.val = val; this.next = next; }
 * }
 */
class Solution {
    public ListNode removeNthFromEnd(ListNode head, int n) {
        ListNode slow = head;
        ListNode fast = head;
        for (int i = 0; i < n; i++) {
            if (fast.next == null) {
                // ç§»é™¤ç¬¬ä¸€ä¸ª
                if (i == n - 1) {
                    return head.next;
                } else {
                    // ç§»é™¤çš„ä¸ªæ•°å¤šäºæœ‰æ•ˆçš„é•¿åº¦
                    return head;
                }

            }
            fast = fast.next;
        }
        while (fast.next != null) {
            fast = fast.next;
            slow = slow.next;
        }
        if (slow.next != null) {
            slow.next = slow.next.next;
        }
        return head;
    }
}
